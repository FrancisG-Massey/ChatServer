package com.sundays.chat.io.jdbc;

public class ChannelUserMatcher {

	public final int channelID, userID;

	public ChannelUserMatcher (int channelID, int userID) {
		this.channelID = channelID;
		this.userID = userID;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + channelID;
		result = prime * result + userID;
		return result;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		ChannelUserMatcher other = (ChannelUserMatcher) obj;
		if (channelID != other.channelID) {
			return false;
		}
		if (userID != other.userID) {
			return false;
		}
		return true;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "ChannelUserMatcher [channelID=" + channelID + ", userID="
				+ userID + "]";
	}
}
